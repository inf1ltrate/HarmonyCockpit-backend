-- 10. 视图
create view xuqing_car(车主昵称,汽车名,汽车品牌,汽车里程)
as select username,carname,brand,miles
from user join info on user.user_id = info.user_id
where username = '许青';

create view xuqing_carstate(车主昵称,汽车名,空调开关,空调温度,左转,右转,操作时间)
as select username,carname,ac_on,ac_temp,left1,right1,time
from user join info on user.user_id = info.user_id
join state on info.car_id = state.car_id
where username = '许青';

